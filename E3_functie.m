function E3_functie(a) %functia 'operatii complexe' 
    
  mean (real(a)) % se face media partilor reale ale matricei a
  n=length(a);
  for i=1:1:n % for de la 1 la n, i = i+1
      b(i) = a(i)^2; %vectorul patratelor elem lui a
  end
  b(i) %se afiseaza matricea b
  (a)*(a.') % a inmultit cu a transpus
  
  end 
  
